class SkinDumpHTML extends SkinTemplate {
	/** Using DumpHTML. */
	function initPage( OutputPage $out ) {
		SkinTemplate::initPage( $out );
		$this->skinname  = 'DumpHTML';
		$this->stylename = 'DumpHTML';
		$this->template  = 'DumpHTMLTemplate';
	}
}

